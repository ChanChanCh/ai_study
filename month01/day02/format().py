# format : 양식화

# 흔히 들었던 form
# 이력서 form , 자기소개서 form
# 여기서의 form이란 양식이라는 걸 알 수있다

print("내 키는 {}cm")

print("내 키는 {}cm".format(185))

#format이란 내가 작성한 양식에 데이터를 채워넣는 것 이다.

# 괄호가 2개이상일시 format() 안에 ,으로 구분하여 데이터를 넣어준다.
print("내 키는 {}cm, 내 나이는 {}살이야".format(185,100))

