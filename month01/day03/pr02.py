
number = input("입력")
num = int(number)

if num % 2 == 0:
    print("{}은 2로 나누어 떨어지는 숫자입니다.".format(num)) 
else :
    print("{}은 2로 나누어 떨어지는 숫자입니다.".format(num)) 

if num % 3 == 0:
    print("{}은 3로 나누어 떨어지는 숫자입니다.".format(num)) 
else :
    print("{}은 3로 나누어 떨어지는 숫자입니다.".format(num)) 

if num % 4 == 0:
    print("{}은 4로 나누어 떨어지는 숫자입니다.".format(num)) 
else :
    print("{}은 4로 나누어 떨어지는 숫자입니다.".format(num)) 

if num % 5 == 0:
    print("{}은 5로 나누어 떨어지는 숫자입니다.".format(num)) 
else :
    print(f"{num}은 5로 나누어 떨어지는 숫자입니다.")    
      